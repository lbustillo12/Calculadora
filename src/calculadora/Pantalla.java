    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        // TODO add your handling code here:
        double resultado = 0;
        DecimalFormat df = new DecimalFormat("#.######");

    try {
    numeros[1] = Double.parseDouble(txtNum.getText());

    if (division == true) {
        if (numeros[1] == 0.0) {
            txtNum.setText("Error");
        } else {
            resultado = numeros[0] / numeros[1];
            txtNum.setText(df.format(resultado));
        }
        } else if (multi == true) {
        resultado = numeros[0] * numeros[1];
        txtNum.setText(df.format(resultado));
        } else if (resta == true) {
        resultado = numeros[0] - numeros[1];
        txtNum.setText(df.format(resultado));
        } else if (suma == true) {
        resultado = numeros[0] + numeros[1];
        txtNum.setText(df.format(resultado));
        } else if (porc == true) {
        resultado = numeros[1] * (numeros[0] / 100.0);
        txtNum.setText(df.format(resultado));
    }
    } catch (Exception e) {
    
 }

        decimal = true;
        division = false;
        multi = false;
        suma = false;
        resta = false;
        porc = false;
        numeros[0] = 0;
        numeros[1] = 0;

    }//GEN-LAST:event_btnIgualActionPerformed

